


// function evolution
// function statement/ declaration
function sum(num1, num2) {
    console.log(num1 + num2);
}

sum(17, 5);




// function expression
const minus = function(num1, num2) {
    console.log(num1 - num2);
}
minus(23, 8)





// arrow function
const division = (num1, num2) => {
    console.log(num1 / num2);
}

division(9, 3);



const multiply = num => num * num;
console.log(multiply(9));



const sayHi = () => 'Hi Guys';
console.log(sayHi());















