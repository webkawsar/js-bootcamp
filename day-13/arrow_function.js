


// Normal function 
function sayHi() {
    console.log('Hi');
}
sayHi()


// Arrow Function
const sayHello = () => {
    console.log('Hello')
}
sayHello();


const sum  = (num1, num2) => {
    return num1 + num2;
}
console.log(sum(3, 2));


const square = num => num * num;
console.log(square(3));



