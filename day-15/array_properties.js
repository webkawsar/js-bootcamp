

// Array
const arr = ['I', 'love', 'JavaScript', 'React.js', 'Vue.js', 'Angular'];

// find length
console.log(arr.length);

// access element
console.log(arr[0]);
console.log(arr[1]);

// add or remove element from end of array
console.log(arr.push('Yeah'));
console.log(arr.pop()); // modify array
console.log(arr.length);

// add or remove element from start of array
console.log(arr.unshift('Hi Guys'));
console.log(arr.shift()); // modify array
console.log(arr.length);

//cutting array
console.log(arr.slice());
console.log(arr.slice(2));
console.log(arr.slice(2, 3));

// console.log(arr.splice(2));
console.log(arr.splice(4, 1));

console.log(arr);

// array concatenation
console.log(arr.concat('Vue.js'));


// empty array
console.log(arr.length = 0);


console.log(arr);














